public class Person {
    
    String fname;
    String lname;
    String address;
    String email;
    String password;
    String phone;

    public Person(String _fname, String _lname, String _address, String _email, String _password, String _phone){
        this.fname = _fname;
        this.lname = _lname;
        this.address = _address;
        this.email = _email;
        this.password = _password;
        this.phone = _phone;
    }

}